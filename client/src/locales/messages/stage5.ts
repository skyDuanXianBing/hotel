const toTraditional = (text: string) =>
  text
    .split('')
    .map((char) => traditionalCharacterMap[char] || char)
    .join('')

const mapMessageStrings = <T>(value: T, mapper: (text: string) => string): T => {
  if (typeof value === 'string') {
    return mapper(value) as T
  }

  if (Array.isArray(value)) {
    return value.map((item) => mapMessageStrings(item, mapper)) as T
  }

  if (value && typeof value === 'object') {
    return Object.fromEntries(
      Object.entries(value as Record<string, unknown>).map(([key, item]) => [
        key,
        mapMessageStrings(item, mapper),
      ]),
    ) as T
  }

  return value
}

