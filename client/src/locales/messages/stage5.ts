type DeepPartial<T> = {
  [K in keyof T]?: T[K] extends Array<infer U>
    ? Array<DeepPartial<U>>
    : T[K] extends object
      ? DeepPartial<T[K]>
      : T[K] extends string
        ? string
        : T[K]
}

const cloneMessages = <T>(value: T): T => JSON.parse(JSON.stringify(value)) as T

const mergeMessages = <T extends Record<string, unknown>>(base: T, override: DeepPartial<T>): T => {
  const output = cloneMessages(base) as Record<string, unknown>

  Object.entries(override).forEach(([key, value]) => {
    if (
      value &&
      typeof value === 'object' &&
      !Array.isArray(value) &&
      output[key] &&
      typeof output[key] === 'object' &&
      !Array.isArray(output[key])
    ) {
      output[key] = mergeMessages(
        output[key] as Record<string, unknown>,
        value as Record<string, unknown>,
      )
      return
    }

    output[key] = value as unknown
  })

  return output as T
}

